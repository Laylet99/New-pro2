    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
           SpellService ss=new SpellService();
           SpellServiceSoap proxy=ss.getSpellServiceSoap();
           
           CheckTextRequest req=new CheckTextRequest();
           req.setLang("ru");
           req.setText(jTextArea1.getText());
           
           CheckTextResponse resp=new CheckTextResponse();
           resp=proxy.checkText(req);

           String s="";
           SpellResult result=resp.getSpellResult();
           for(SpellError serr : result.getError()){
                    s=s+serr.getWord()+" : ";
                    for(String si : serr.getS())       s=s+si+" ";
                    s=s+"\n";
           }
           jTextArea2.setText(s);
    }//GEN-LAST:event_jButton1ActionPerformed

